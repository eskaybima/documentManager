
package com.fasyl.vo;

/**
 *
 * @author OYEWOLE
 */
public class AccessControl {
    private String access_key;
    private String access_nature;
    private String ref_id;

    /**
     * @return the access_key
     */
    public String getAccess_key() {
        return access_key;
    }

    /**
     * @param access_key the access_key to set
     */
    public void setAccess_key(String access_key) {
        this.access_key = access_key;
    }

    /**
     * @return the access_nature
     */
    public String getAccess_nature() {
        return access_nature;
    }

    /**
     * @param access_nature the access_nature to set
     */
    public void setAccess_nature(String access_nature) {
        this.access_nature = access_nature;
    }

    /**
     * @return the ref_id
     */
    public String getRef_id() {
        return ref_id;
    }

    /**
     * @param ref_id the ref_id to set
     */
    public void setRef_id(String ref_id) {
        this.ref_id = ref_id;
    }
}
